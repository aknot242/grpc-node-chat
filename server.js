//Receive message from client joining
function join(call, callback) {
  users.push(call);
  notifyChat({ user: "Server", text: "new user joined..." });
}

//Receive message from client
function send(call, callback) {
  notifyChat(call.request);
}

//Send message to all connected clients
function notifyChat(message) {
  users.forEach(user => {
    user.write(message);
  });
}

//Define server with the methods and start it
server.addService(proto.example.Chat.service, { join: join, send: send });

server.bind(target, grpc.ServerCredentials.createInsecure());

server.start();

console.log("Server started");

function welcomeAll() {
  if (users.length > 0 ) {
    const randomCountry = countries[Math.floor(Math.random() * countries.length)];
    console.log(`Welcoming users from ${randomCountry}`);
    notifyChat({ user: "Server", text: `Welcoming all users! Anyone here from ${randomCountry}?`});
  }
}

